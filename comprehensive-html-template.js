function generateCategorySection(category, issues) {
  const sectionId = category.toLowerCase().replace(/[^a-z0-9]/g, '-');
  
  let html = `<div class="category-section" id="category-${sectionId}">`;
  html += `<div class="category-header">`;
  html += `<h3 class="category-title">${category} <span class="issue-count">(${issues.length})</span></h3>`;
  html += `<button class="copy-category-btn" onclick="copyCategoryIssues('${sectionId}')">ðŸ“‹ Copy All Issues</button>`;
  html += `</div>`;
  
  // Group by page within category
  const pageGroups = {};
  issues.forEach(issue => {
    const pageUrl = issue.pageUrl || 'unknown';
    if (!pageGroups[pageUrl]) {
      pageGroups[pageUrl] = [];
    }
    pageGroups[pageUrl].push(issue);
  });
  
  Object.entries(pageGroups).forEach(([pageUrl, pageIssues]) => {
    const pageTitle = pageIssues[0]?.pageTitle || 'Unknown Page';
    html += generatePageIssuesSection(pageUrl, pageTitle, pageIssues);
  });
  
  html += '</div>';
  return html;
}

function generatePageIssuesSection(pageUrl, pageTitle, issues) {
  let html = `<div class="page-issues-section">`;
  html += `<div class="page-header">`;
  html += `<h4 class="page-title">${getPageName(pageUrl)}</h4>`;
  html += `<div class="page-meta">`;
  html += `<span class="page-url">${pageUrl}</span>`;
  html += `<span class="page-issue-count">${issues.length} issues</span>`;
  html += `</div>`;
  html += `</div>`;
  
  html += `<div class="issues-list">`;
  
  issues.forEach((issue, index) => {
    html += generateIssueItem(issue, index);
  });
  
  html += `</div></div>`;
  return html;
}

function generateIssueItem(issue, index) {
  const severityClass = issue.type || 'info';
  const issueId = `issue-${Date.now()}-${index}`;
  
  let html = `<div class="issue-item ${severityClass}" id="${issueId}">`;
  html += `<div class="issue-header">`;
  html += `<div class="issue-type-badge ${severityClass}">${(issue.type || 'INFO').toUpperCase()}</div>`;
  html += `<div class="issue-source">${issue.source || 'unknown'}</div>`;
  html += `<button class="copy-issue-btn" onclick="copyIssue('${issueId}')">ðŸ“‹</button>`;
  html += `</div>`;
  
  html += `<div class="issue-content">`;
  html += `<div class="issue-message">${escapeHtml(issue.message || '')}</div>`;
  
  if (issue.code) {
    html += `<div class="issue-code"><strong>Code:</strong> ${escapeHtml(issue.code)}</div>`;
  }
  
  if (issue.selector) {
    html += `<div class="issue-selector"><strong>Element:</strong> <code>${escapeHtml(issue.selector)}</code></div>`;
  }
  
  if (issue.context) {
    html += `<div class="issue-context"><strong>Context:</strong> <code>${escapeHtml(issue.context)}</code></div>`;
  }
  
  if (issue.help) {
    html += `<div class="issue-help"><strong>Help:</strong> ${escapeHtml(issue.help)}</div>`;
  }
  
  if (issue.helpUrl) {
    html += `<div class="issue-help-url"><strong>More Info:</strong> <a href="${escapeHtml(issue.helpUrl)}" target="_blank">${escapeHtml(issue.helpUrl)}</a></div>`;
  }
  
  // AI-friendly format for copying
  html += `<div class="issue-ai-format" style="display: none;">`;
  html += `Issue: ${issue.message || ''}\\n`;
  if (issue.selector) html += `Element: ${issue.selector}\\n`;
  if (issue.context) html += `HTML Context: ${issue.context}\\n`;
  if (issue.code) html += `Rule: ${issue.code}\\n`;
  html += `Page: ${issue.pageUrl || ''}\\n`;
  html += `Severity: ${issue.type || ''}\\n`;
  if (issue.help) html += `Fix: ${issue.help}\\n`;
  html += `---\\n`;
  html += `</div>`;
  
  html += `</div></div>`;
  return html;
}

