        // Copy to Clipboard Funktion (silent - no toast)
        function copyToClipboard(tableId) {
            const table = document.getElementById(tableId);
            if (!table) return;

            // Erstelle eine temporäre Textarea für das Kopieren
            const textarea = document.createElement('textarea');
            textarea.value = table.innerText;
            document.body.appendChild(textarea);
            textarea.select();
            
            try {
                document.execCommand('copy');
                // Silent copy - no notification
            } catch (err) {
                console.log('Copy failed');
            }
            
            document.body.removeChild(textarea);
        }

        // Smooth Scrolling für Navigation
        function initSmoothScrolling() {
            document.querySelectorAll('a[href^="#"]').forEach(anchor => {
                anchor.addEventListener('click', function (e) {
                    e.preventDefault();
                    const target = document.querySelector(this.getAttribute('href'));
                    if (target) {
                        target.scrollIntoView({
                            behavior: 'smooth',
                            block: 'start'
                        });
                    }
                });
            });
        }

        // Dark Mode Toggle
        function initDarkMode() {
            const prefersDark = window.matchMedia('(prefers-color-scheme: dark)');
            
            function updateTheme() {
                document.documentElement.classList.toggle('dark', prefersDark.matches);
            }
            
            prefersDark.addEventListener('change', updateTheme);
            updateTheme();
        }

        // Filter Badge System
        function initFilterSystem() {
            const badges = document.querySelectorAll('.filter-badge');
            const sections = document.querySelectorAll('.report-section, .dashboard');
            
            badges.forEach(badge => {
                badge.addEventListener('click', function() {
                    const targetSection = this.getAttribute('data-section');
                    
                    // Toggle badge state
                    this.classList.toggle('active');
                    
                    // Show/hide corresponding section
                    const section = document.getElementById(targetSection);
                    if (section) {
                        if (this.classList.contains('active')) {
                            section.classList.remove('hidden');
                            section.style.display = 'block';
                        } else {
                            section.classList.add('hidden');
                            section.style.display = 'none';
                        }
                    }
                    
                    // Update badge appearance based on active state
                    updateBadgeStates();
                });
            });
            
            // Initialize: Set all badges as active and show all sections by default
            // (User can hide sections they don't want to see)
            badges.forEach(function(badge) {
                badge.classList.add('active');
            });
            
            sections.forEach(function(section) {
                section.classList.remove('hidden');
                section.style.display = 'block';
            });
        }
        
        function updateBadgeStates() {
            const badges = document.querySelectorAll('.filter-badge');
            const activeBadges = document.querySelectorAll('.filter-badge.active');
            
            badges.forEach(badge => {
                if (activeBadges.length === 0) {
                    badge.classList.remove('inactive');
                } else {
                    if (badge.classList.contains('active')) {
                        badge.classList.remove('inactive');
                    } else {
                        badge.classList.add('inactive');
                    }
                }
            });
        }

